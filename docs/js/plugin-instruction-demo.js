/**
 * jsPsych plugin for instruction demonstrations
 * Shows gridworld animations without requiring user responses
 */

class InstructionDemoPlugin {
    constructor(jsPsych) {
        this.jsPsych = jsPsych;
    }

    static info = {
        name: 'instruction-demo',
        parameters: {
            trial_data: {
                type: 'object',
                pretty_name: 'Trial Data',
                default: undefined,
                description: 'The trial object generated by trial-generator.js'
            },
            prompt: {
                type: 'string',
                pretty_name: 'Prompt',
                default: 'Watch the demonstration:',
                description: 'Text to display above the animation'
            },
            auto_advance: {
                type: 'boolean',
                pretty_name: 'Auto Advance',
                default: false,
                description: 'Whether to automatically advance after animation completes'
            },
            advance_delay: {
                type: 'number',
                pretty_name: 'Advance Delay',
                default: 2000,
                description: 'Delay in ms before auto advancing (if auto_advance is true)'
            }
        }
    };

    trial(display_element, trial) {
        let html = `
            <div class="instruction-demo-container" style="text-align: center; padding: 20px;">
                <div class="demo-prompt" style="margin-bottom: 20px;">
                    <p style="font-size: 18px; margin-bottom: 10px;">${trial.prompt}</p>
                </div>
                <div class="demo-stimulus">
                    <canvas id="demoCanvas" class="demo-canvas" style="margin: 10px auto; display: block;"></canvas>
                    <div class="demo-buttons" style="margin-top: 15px;">
                        <button id="backDemoBtn" class="jspsych-btn">Previous</button>
                        <button id="continueDemoBtn" class="jspsych-btn" disabled>Next</button>
                    </div>
                </div>
            </div>
        `;
        display_element.innerHTML = html;
        
        const startTime = performance.now();
        const continueBtn = document.getElementById('continueDemoBtn');
        const backBtn = document.getElementById('backDemoBtn');

        const canvas = document.getElementById('demoCanvas');
        const renderer = new GridWorldRenderer(canvas, {
            laneLength: 19, 
            maxWidth: 1000, 
            laneHeight: 90, 
            animationSpeed: 700 
        });

        // Load images
        const imageTypes = ['farmer', 'wizard', 'wizard-wand', 'lightning', 'apple', 'banana', 'rock'];
        const imagePromises = imageTypes.map(type => new Promise(resolve => {
            const img = new Image();
            img.onload = () => {
                renderer.images.set(type, img);
                resolve();
            };
            img.onerror = resolve;
            img.src = `images/${type}.png`;
        }));

        Promise.all(imagePromises).then(() => {
            renderer.renderFrame(trial.trial_data, 0);
            // Auto-start the looping animation
            this.startLoopingAnimation(renderer, trial.trial_data, continueBtn);
        });

        // Event listeners for navigation
        continueBtn.addEventListener('click', () => {
            this.endTrial(startTime, 'forward');
        });

        backBtn.addEventListener('click', () => {
            this.endTrial(startTime, 'backward');
        });

        // Keyboard event listeners
        const keyListener = (e) => {
            if (e.key === 'ArrowRight' && !continueBtn.disabled) {
                this.endTrial(startTime, 'forward');
            } else if (e.key === 'ArrowLeft') {
                this.endTrial(startTime, 'backward');
            }
        };

        document.addEventListener('keydown', keyListener);
        
        // Store the listener to remove it later
        this.keyListener = keyListener;
    }

    startLoopingAnimation(renderer, trialData, continueBtn) {
        let hasCompletedFirstLoop = false;
        
        // Start the first animation (non-looping to track completion)
        renderer.playAnimation(trialData, {
            loop: false,
            onComplete: () => {
                // First loop completed - enable continue button
                if (!hasCompletedFirstLoop) {
                    hasCompletedFirstLoop = true;
                    continueBtn.disabled = false;
                }
                
                // Start continuous looping
                setTimeout(() => {
                    renderer.playAnimation(trialData, {
                        loop: true,
                        onComplete: () => {
                        }
                    });
                }, 1000); // Small delay before starting loop
            }
        });
    }

    endTrial(startTime, direction = 'forward') {
        // Clean up keyboard listener
        if (this.keyListener) {
            document.removeEventListener('keydown', this.keyListener);
        }

        const trial_data = {
            rt: Math.round(performance.now() - startTime),
            trial_type: 'instruction-demo',
            direction: direction
        };
        
        // For now, just store the direction and finish normally
        // Going backward in jsPsych timelines requires more complex setup
        this.jsPsych.finishTrial(trial_data);
        
        if (direction === 'backward') {
            // Alert user that backward navigation is not fully implemented yet
            // In a full implementation, this would integrate with a more complex navigation system
            console.log('Backward navigation requested - this would go to previous section');
        }
    }
}

// Make plugin available globally
if (typeof jsPsychInstructionDemo === 'undefined') {
    var jsPsychInstructionDemo = InstructionDemoPlugin;
} 