class GridworldTrialPlugin {
    constructor(jsPsych) {
        this.jsPsych = jsPsych;
    }

    static info = {
        name: 'gridworld-trial',
        parameters: {
            trial_data: {
                type: 'object',
                pretty_name: 'Trial Data',
                default: undefined,
                description: 'The trial object generated by trial-generator.js'
            }
        }
    };

    trial(display_element, trial) {
        let html = `
            <div class="gw-container">
                <div class="gw-stimulus">
                    <p class="gw-prompt">Click "Play Animation" to see what happened.</p>
                    <canvas id="gridCanvas" class="gw-canvas"></canvas>
                    <div class="gw-buttons">
                        <button id="playBtn" class="jspsych-btn">Play Animation</button>
                        <button id="replayBtn" class="jspsych-btn" style="display: none;">Replay Animation</button>
                    </div>
                </div>
                <div id="gw-question-container" class="gw-question-container" style="visibility: hidden; margin-top: 50px;">
                    <h3>Which sentence best describes what happened?</h3>
                    <form id="gw-form">
                        <div class="gw-option" style="margin-bottom: 10px;">
                            <input type="radio" name="causalQuestion" id="opt1" value="caused" required>
                            <label for="opt1">The wizard <strong>caused</strong> the farmer to get ${trial.trial_data.outcome}.</label>
                        </div>
                        <div class="gw-option" style="margin-bottom: 10px;">
                            <input type="radio" name="causalQuestion" id="opt2" value="enabled" required>
                            <label for="opt2">The wizard <strong>enabled</strong> the farmer to get ${trial.trial_data.outcome}.</label>
                        </div>
                        <div class="gw-option" style="margin-bottom: 10px;">
                            <input type="radio" name="causalQuestion" id="opt3" value="allowed" required>
                            <label for="opt3">The wizard <strong>allowed</strong> the farmer to get ${trial.trial_data.outcome}.</label>
                        </div>
                        <div class="gw-option" style="margin-bottom: 10px;">
                            <input type="radio" name="causalQuestion" id="opt4" value="no_difference" required>
                            <label for="opt4">The wizard <strong>made no difference</strong> to the farmer getting ${trial.trial_data.outcome}.</label>
                        </div>
                        <button type="submit" id="submitBtn" class="jspsych-btn btn-blue" style="margin-top: 20px;" disabled>Submit</button>
                    </form>
                </div>
            </div>
        `;
        display_element.innerHTML = html;
        const startTime = performance.now();

        const canvas = document.getElementById('gridCanvas');
        const renderer = new GridWorldRenderer(canvas, {
            laneLength: 19, maxWidth: 1000, laneHeight: 90,
            animationSpeed: 500,
            imageBasePath: '../../images'
        });

        const imageTypes = ['farmer', 'wizard', 'wizard-wand', 'lightning', 'apple', 'banana', 'rock', 'thought'];
        const imagePromises = imageTypes.map(type => new Promise(resolve => {
            const img = new Image();
            img.onload = resolve;
            img.onerror = resolve;
            img.src = `../../images/${type}.png`;
            renderer.images.set(type, img);
        }));

        Promise.all(imagePromises).then(() => {
            renderer.renderFrame(trial.trial_data, 0);
        });

        const playBtn = document.getElementById('playBtn');
        const replayBtn = document.getElementById('replayBtn');
        const submitBtn = document.getElementById('submitBtn');
        const radioButtons = display_element.querySelectorAll('input[name="causalQuestion"]');
        const prompt = document.querySelector('.gw-prompt');

        const playAnimation = (isReplay = false) => {
            replayBtn.disabled = true;
            if (!isReplay) {
                playBtn.disabled = true;
            }
            renderer.playAnimation(trial.trial_data, {
                loop: false,
                onComplete: () => {
                    if (!isReplay) {
                        playBtn.style.display = 'none';
                        replayBtn.style.display = 'inline-block';
                        document.getElementById('gw-question-container').style.visibility = 'visible';
                    }
                    replayBtn.disabled = false;
                    prompt.innerText = 'Please answer the question below.';
                }
            });
        };

        playBtn.addEventListener('click', () => playAnimation(false));
        replayBtn.addEventListener('click', () => playAnimation(true));

        radioButtons.forEach(radio => {
            radio.addEventListener('change', () => {
                submitBtn.disabled = false;
            });
        });

        document.getElementById('gw-form').addEventListener('submit', (e) => {
            e.preventDefault();
            const trial_data_to_save = {
                task: 'speaker',
                trial_id: trial.trial_data.id,
                response: document.querySelector('input[name="causalQuestion"]:checked').value,
                rt: Math.round(performance.now() - startTime)
            };

            display_element.innerHTML = '';
            this.jsPsych.finishTrial(trial_data_to_save);
        });
    }
}

