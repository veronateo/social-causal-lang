// Helper function to generate all 4 utterance options
function generateAllUtterances(outcome) {
    const verbs = [
        { value: 'caused', text: 'caused', label: `The wizard <strong>caused</strong> the farmer to get the ${outcome}.` },
        { value: 'enabled', text: 'enabled', label: `The wizard <strong>enabled</strong> the farmer to get the ${outcome}.` },
        { value: 'allowed', text: 'allowed', label: `The wizard <strong>allowed</strong> the farmer to get the ${outcome}.` },
        { value: 'made_no_difference', text: 'made no difference', label: `The wizard <strong>made no difference</strong> to the farmer getting the ${outcome}.` }
    ];
    return verbs;
}

class ListenerTrialPlugin {
    constructor(jsPsych) {
        this.jsPsych = jsPsych;
    }

    static info = {
        name: 'listener-trial',
        parameters: {
            trial_data: {
                type: 'object',
                pretty_name: 'Trial Data',
                default: undefined,
                description: 'Trial data from listener CSV'
            }
        }
    };

    trial(display_element, trial) {
        const trialData = trial.trial_data;

        // Generate trial objects from trial IDs
        const generator = new TrialGenerator();
        const scenario1AnimationDef = window.TrialDefinitions.trialDefinitionsMap[trialData.scenario1_animation];
        const scenario2AnimationDef = window.TrialDefinitions.trialDefinitionsMap[trialData.scenario2_animation];
        const scenario1Animation = generator.generateTrial(scenario1AnimationDef);
        const scenario2Animation = generator.generateTrial(scenario2AnimationDef);

        // Generate utterance display based on experiment mode
        let utteranceHTML = '';

        if (SHOW_ALL_UTTERANCES) {
            // New version: Show all 4 utterance options with one selected
            const allUtterances = generateAllUtterances(trialData.outcome);
            const selectedVerb = trialData.verb;

            utteranceHTML = `
                <div class="listener-prompt" style="text-align: center; margin-bottom: 20px;">
                    <p style="font-size: 1.2em; font-style: italic; margin-bottom: 20px">The describer made the following selection:</p>
                    <div class="utterance-options" style="max-width: 650px; margin: 0 auto; display: flex; flex-direction: column; align-items: center;">
                        ${allUtterances.map(option => {
                const isSelected = option.value === selectedVerb;
                return `
                                <div class="utterance-option ${isSelected ? 'selected' : ''}" style="margin-bottom: 8px;">
                                    <input type="radio"
                                           id="utterance_${option.value}"
                                           name="utterance_display"
                                           value="${option.value}"
                                           ${isSelected ? 'checked' : ''}>
                                    <label for="utterance_${option.value}">
                                        ${option.label}
                                    </label>
                                </div>
                            `;
            }).join('')}
                    </div>
                </div>
            `;
        } else {
            // Original version: Show only the selected utterance
            const utteranceWithBoldVerb = trialData.utterance
                .replace(/\bcaused\b/g, '<strong>caused</strong>')
                .replace(/\benabled\b/g, '<strong>enabled</strong>')
                .replace(/\ballowed\b/g, '<strong>allowed</strong>')
                .replace(/made no difference/g, '<strong>made no difference</strong>');

            utteranceHTML = `
                <div class="listener-prompt" style="text-align: center; margin-bottom: 20px;">
                    <p>The describer chose the following sentence:</p>
                    <p style="font-size: 1.2em; font-style: italic; margin: 15px 0;">"${utteranceWithBoldVerb}"</p>
                </div>
            `;
        }

        let html = `
            <style>
                .ui-slider-pip .ui-slider-label { display: none !important; }
                .utterance-option input[type="radio"] {
                    pointer-events: none;
                }
            </style>
            <div class="listener-container" style="max-width: 1200px; margin: 0 auto;">
                ${utteranceHTML}

                <div class="animations-container" style="margin: 30px 0; display: flex; gap: 20px; justify-content: center;">
                    <div class="animation-wrapper" style="flex: 0 1 auto; border: 2px solid #ccc; padding: 15px; border-radius: 8px;">
                        <p style="text-align: center; font-weight: bold; margin-bottom: 10px;">Scenario 1</p>
                        <canvas id="scenario1Canvas" class="gw-canvas" style="margin: 0 auto; display: block;"></canvas>
                        <div style="text-align: center; margin-top: 10px;">
                            <button id="playScenario1Btn" class="jspsych-btn">Play Animation</button>
                            <button id="replayScenario1Btn" class="jspsych-btn" style="display: none;">Replay Animation</button>
                        </div>
                    </div>

                    <div class="animation-wrapper" style="flex: 0 1 auto; border: 2px solid #ccc; padding: 15px; border-radius: 8px;">
                        <p style="text-align: center; font-weight: bold; margin-bottom: 10px;">Scenario 2</p>
                        <canvas id="scenario2Canvas" class="gw-canvas" style="margin: 0 auto; display: block;"></canvas>
                        <div style="text-align: center; margin-top: 10px;">
                            <button id="playScenario2Btn" class="jspsych-btn">Play Animation</button>
                            <button id="replayScenario2Btn" class="jspsych-btn" style="display: none;">Replay Animation</button>
                        </div>
                    </div>
                </div>

                <div id="slider-container" style="visibility: hidden; margin: 40px auto; max-width: 500px;">
                    <h3>Which scenario did the describer see?</h3>
                    <div style="margin: 30px 0; position: relative;">
                        <div id="response-slider"></div>
                        <div style="display: flex; justify-content: space-between; margin-top: 10px; font-size: 14px;">
                            <span style="width: 33.33%; text-align: left;">Definitely Scenario 1</span>
                            <span style="width: 33.33%; text-align: center;">Unsure</span>
                            <span style="width: 33.33%; text-align: right;">Definitely Scenario 2</span>
                        </div>
                    </div>
                    <button type="submit" id="submitBtn" class="jspsych-btn btn-blue" style="margin-top: 20px;" disabled>Submit</button>
                </div>
            </div>
        `;

        display_element.innerHTML = html;
        const startTime = performance.now();

        // Initialize renderers
        const scenario1Canvas = document.getElementById('scenario1Canvas');
        const scenario2Canvas = document.getElementById('scenario2Canvas');

        const scenario1Renderer = new GridWorldRenderer(scenario1Canvas, {
            laneLength: 19, maxWidth: 620, laneHeight: 60,
            animationSpeed: 500,
            imageBasePath: '../../images'
        });

        const scenario2Renderer = new GridWorldRenderer(scenario2Canvas, {
            laneLength: 19, maxWidth: 620, laneHeight: 60,
            animationSpeed: 500,
            imageBasePath: '../../images'
        });

        // Load images
        const imageTypes = ['farmer', 'wizard', 'wizard-wand', 'lightning', 'apple', 'banana', 'rock', 'thought'];
        const imagePromises = imageTypes.map(type => new Promise(resolve => {
            const img = new Image();
            img.onload = resolve;
            img.onerror = resolve;
            img.src = `../../images/${type}.png`;
            scenario1Renderer.images.set(type, img);
            scenario2Renderer.images.set(type, img);
        }));

        Promise.all(imagePromises).then(() => {
            // Render initial frames
            scenario1Renderer.renderFrame(scenario1Animation, 0);
            scenario2Renderer.renderFrame(scenario2Animation, 0);
        });

        // State tracking
        let scenario1Played = false;
        let scenario2Played = false;

        // Button elements
        const playScenario1Btn = document.getElementById('playScenario1Btn');
        const replayScenario1Btn = document.getElementById('replayScenario1Btn');
        const playScenario2Btn = document.getElementById('playScenario2Btn');
        const replayScenario2Btn = document.getElementById('replayScenario2Btn');
        const sliderContainer = document.getElementById('slider-container');
        const submitBtn = document.getElementById('submitBtn');

        // Disable scenario 2 button initially - must play scenario 1 first
        playScenario2Btn.disabled = true;

        // Initialize jQuery UI slider immediately (but keep container hidden) to reserve space
        $('#response-slider').slider({
            value: 50,
            min: 0,
            max: 100,
            step: 1,
            slide: function (event, ui) {
                submitBtn.disabled = false;
            },
            change: function (event, ui) {
                submitBtn.disabled = false;
            }
        }).slider("pips", {
            labels: false,
            step: 50
        });

        // Animation functions
        const playScenario1Animation = () => {
            playScenario1Btn.disabled = true;
            replayScenario1Btn.disabled = true;
            scenario1Renderer.playAnimation(scenario1Animation, {
                loop: false,
                onComplete: () => {
                    playScenario1Btn.style.display = 'none';
                    replayScenario1Btn.style.display = 'inline-block';
                    replayScenario1Btn.disabled = false;
                    scenario1Played = true;
                    // Enable scenario 2 button after scenario 1 is played for the first time
                    if (!scenario2Played) {
                        playScenario2Btn.disabled = false;
                    }
                    checkBothPlayed();
                }
            });
        };

        const playScenario2Animation = () => {
            playScenario2Btn.disabled = true;
            replayScenario2Btn.disabled = true;
            scenario2Renderer.playAnimation(scenario2Animation, {
                loop: false,
                onComplete: () => {
                    playScenario2Btn.style.display = 'none';
                    replayScenario2Btn.style.display = 'inline-block';
                    replayScenario2Btn.disabled = false;
                    scenario2Played = true;
                    checkBothPlayed();
                }
            });
        };

        const checkBothPlayed = () => {
            if (scenario1Played && scenario2Played) {
                sliderContainer.style.visibility = 'visible';
            }
        };

        // Event listeners
        playScenario1Btn.addEventListener('click', playScenario1Animation);
        replayScenario1Btn.addEventListener('click', playScenario1Animation);
        playScenario2Btn.addEventListener('click', playScenario2Animation);
        replayScenario2Btn.addEventListener('click', playScenario2Animation);

        submitBtn.addEventListener('click', () => {
            const response = $('#response-slider').slider('value');
            const rt = Math.round(performance.now() - startTime);

            const trial_data_to_save = {
                task: 'listener',
                trial_number: trialData.trial_number,
                verb: trialData.verb,
                outcome: trialData.outcome,
                utterance: trialData.utterance,
                scenario1_animation: trialData.scenario1_animation,
                scenario2_animation: trialData.scenario2_animation,
                modal_animation: trialData.modal_animation,
                modal_position: trialData.modal_position,
                response: response,
                rt: rt,
                p_scenario1: trialData.p_scenario1,
                p_scenario2: trialData.p_scenario2,
                model_prediction: trialData.model_prediction,
                discrimination_strength: trialData.discrimination_strength
            };

            // console.log('Submitting trial data:', trial_data_to_save);
            display_element.innerHTML = '';
            this.jsPsych.finishTrial(trial_data_to_save);
        });
    }
}

