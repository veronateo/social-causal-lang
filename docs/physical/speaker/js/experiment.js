// Attention Check 
const attention_check = {
    type: jsPsychSurveyHtmlForm,
    preamble: '<h3>Comprehension Check</h3><p>Please answer the following questions to continue.</p>',
    html: `
        <div class="jspsych-content" style="max-width: 700px; text-align: left;">
            <p style="font-weight: bold; margin-bottom: 1em;">1. What is the farmer's goal in the animations? <span style="color: red;">*</span></p>
            <div style="padding-left: 2em;"> 
                <p><input type="radio" name="farmer_goal" value="To talk to the wizard" required> To talk to the wizard</p>
                <p><input type="radio" name="farmer_goal" value="To get a piece of fruit" required> To get a piece of fruit</p>
                <p><input type="radio" name="farmer_goal" value="To place or remove a rock" required> To place or remove a rock</p>
                <p><input type="radio" name="farmer_goal" value="To build a house" required> To build a house</p>
            </div>
            <p style="margin-top: 2em; font-weight: bold; margin-bottom: 1em;">2. Which of the following can the wizard do? (select all that apply) <span style="color: red;">*</span></p>
            <div style="padding-left: 2em;">
                <p><input type="checkbox" name="wizard_rain" value="Make it rain"> Make it rain</p>
                <p><input type="checkbox" name="wizard_place" value="Place a rock"> Place a rock</p>
                <p><input type="checkbox" name="wizard_remove" value="Remove a rock"> Remove a rock</p>
                <p><input type="checkbox" name="wizard_nothing" value="Do nothing"> Do nothing</p>
                <p><input type="checkbox" name="wizard_fruits" value="Change the fruits"> Change the fruits</p>
            </div>
        </div>
    `,
    button_label: 'Submit',
    on_load: function () {
        setTimeout(() => {
            const submitButton = document.querySelector('#jspsych-survey-html-form-next');

            if (submitButton) {
                // Initially disable submit button
                submitButton.disabled = true;
                submitButton.style.opacity = '0.5';
                submitButton.style.cursor = 'not-allowed';

                // Check if both questions are answered
                const checkFormValidity = () => {
                    const radioButton = document.querySelector('input[name="farmer_goal"]:checked');
                    const checkboxes = document.querySelectorAll('input[type="checkbox"]:checked');

                    const isRadioSelected = radioButton !== null;
                    const isAnyCheckboxChecked = checkboxes.length > 0;

                    if (isRadioSelected && isAnyCheckboxChecked) {
                        submitButton.disabled = false;
                        submitButton.style.opacity = '1';
                        submitButton.style.cursor = 'pointer';
                    } else {
                        submitButton.disabled = true;
                        submitButton.style.opacity = '0.5';
                        submitButton.style.cursor = 'not-allowed';
                    }
                };

                const radioButtons = document.querySelectorAll('input[name="farmer_goal"]');
                const checkboxes = document.querySelectorAll('input[type="checkbox"]');

                radioButtons.forEach(radio => {
                    radio.addEventListener('change', checkFormValidity);
                });

                checkboxes.forEach(checkbox => {
                    checkbox.addEventListener('change', checkFormValidity);
                });
            }
        }, 100);
    },
    on_finish: function (data) {
        const correct_farmer = data.response.farmer_goal === 'To get a piece of fruit';
        const correct_wizard =
            data.response.wizard_place &&
            data.response.wizard_remove &&
            data.response.wizard_nothing &&
            !data.response.wizard_rain &&
            !data.response.wizard_fruits;

        data.correct = correct_farmer && correct_wizard;
    }
};


const failed_check_message = {
    type: jsPsychHtmlButtonResponse,
    stimulus: "<p>You missed a question. Please carefully read the instructions again.</p>",
    choices: ['Continue']
};

const if_node = {
    timeline: [failed_check_message],
    conditional_function: function () {
        const last_trial_correct = jsPsych.data.get().last(1).values()[0].correct;
        return !last_trial_correct;
    }
};


// Instructions and comprehension loop
const instructions_and_comprehension_loop = {
    timeline: [
        {
            ...instructions,
            on_finish: function (data) {
                if (instructions.on_finish) {
                    instructions.on_finish(data);
                }
                jsPsych.setProgressBar(0.25);
            }
        },
        attention_check,
        if_node
    ],
    loop_function: function () {
        const last_check_correct = jsPsych.data.get().filter({ trial_type: 'survey-html-form' }).last(1).values()[0].correct;
        return !last_check_correct;
    },
    on_timeline_finish: function () {
        jsPsych.setProgressBar(0.30);
    }
};
timeline.push(instructions_and_comprehension_loop);

timeline.push({
    type: jsPsychHtmlButtonResponse,
    stimulus: '<p>Nice job! The main trials will now begin.</p>',
    choices: ['Start Trials'],
    on_finish: function () {
        jsPsych.setProgressBar(0.35);
    }
});


// Main Trials
const shuffled_trials = jsPsych.randomization.shuffle(window.TrialData.experimentalTrials);
for (let i = 0; i < shuffled_trials.length; i++) {
    const trial = shuffled_trials[i];
    timeline.push({
        type: GridworldTrialPlugin,
        trial_data: trial,
        on_finish: function (data) {
            const trials_completed = i + 1;
            const total_trials = shuffled_trials.length;

            const trial_progress = 0.35 + (trials_completed / total_trials) * 0.5;
            jsPsych.setProgressBar(trial_progress);
        }
    });
}

